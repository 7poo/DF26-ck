def main():
    data_dir = find_data_dir()
    ensure_dir(OUT_DIR)
    print(f"Data dir: {data_dir}\nOutput : {OUT_DIR}\n")

    X_train, X_val, X_test, Y_train, Y_val, feat_cols = load_data(data_dir)
    print("Loading sequences...")
    rows_tr, lens_tr = get_rows_lens(X_train, feat_cols)
    rows_va, lens_va = get_rows_lens(X_val,   feat_cols)
    rows_te, lens_te = get_rows_lens(X_test,  feat_cols)
    y_tr = Y_train[TARGET_COLS].to_numpy(dtype=np.int64)
    y_va = Y_val[TARGET_COLS].to_numpy(dtype=np.int64)
    reg_tr = np.array([get_regime(r) for r in rows_tr])
    reg_va = np.array([get_regime(r) for r in rows_va])
    reg_te = np.array([get_regime(r) for r in rows_te])

    len_stats = {
        split: {
            "n": int(lens.size), "min": int(lens.min()), "mean": float(lens.mean()),
            "median": float(np.median(lens)), "p90": float(np.percentile(lens, 90)),
            "p95": float(np.percentile(lens, 95)), "max": int(lens.max()),
        }
        for split, lens in [("train", lens_tr), ("val", lens_va), ("test", lens_te)]
    }

    print("Analyzing special tokens...")
    spec = analyze_special_tokens(rows_tr, rows_va, rows_te, y_tr, y_va)

    print("Analyzing large codes...")
    large = analyze_large_codes(rows_tr, rows_va, rows_te, y_tr, y_va)

    print("Analyzing regimes...")
    reg_result = analyze_regimes(rows_tr, rows_va, rows_te, y_tr, y_va, lens_tr, lens_va, lens_te)

    print("Analyzing positional rules...")
    pos_results, pos_acc = analyze_positional_rules(rows_tr, rows_va, y_tr, y_va)

    print("Analyzing targets...")
    tgt = analyze_targets(Y_train, Y_val)

    print("Analyzing label quality...")
    quality = analyze_quality(rows_tr, y_tr)

    # Attach boss_104_always_last to reg_result for report
    reg_result["boss_104_always_last"] = spec.get("boss_104_position", {}).get("pct_104_at_last", 0)

    summary = {
        "len_stats": len_stats,
        "special_tokens": spec,
        "large_codes": large,
        "regimes": reg_result,
        "pos_results": pos_results,
        "pos_accuracy_by_position": pos_acc,
        "targets": tgt,
        "quality": quality,
    }

    json_path = os.path.join(OUT_DIR, "eda_summary.json")
    with open(json_path, "w", encoding="utf-8") as f:
        # Convert numpy types for JSON
        def convert(o):
            if isinstance(o, (np.integer,)): return int(o)
            if isinstance(o, (np.floating,)): return float(o)
            if isinstance(o, np.ndarray): return o.tolist()
            if isinstance(o, tuple): return list(o)
            raise TypeError(f"Not serializable: {type(o)}")
        json.dump(summary, f, ensure_ascii=False, indent=2, default=convert)
    print(f"  → JSON summary: {json_path}")

    print("Generating plots...")
    make_all_plots(OUT_DIR, {
        "rows_tr": rows_tr, "rows_va": rows_va, "rows_te": rows_te,
        "lens_tr": lens_tr, "lens_va": lens_va, "lens_te": lens_te,
        "Y_train": Y_train, "Y_val": Y_val,
        "reg_tr": reg_tr, "reg_va": reg_va, "reg_te": reg_te,
        "y_tr": y_tr, "y_va": y_va,
        "large": large, "spec": spec,
        "pos_results": pos_results,
    })

    print("Building report...")
    build_report(summary, os.path.join(OUT_DIR, "EDA_Report_DF26_Deep.md"))

    print("\n=== DONE ===")
    print(f"Output folder : {OUT_DIR}/")
    print("Key files:")
    print("  eda_summary.json")
    print("  EDA_Report_DF26_Deep.md")
    for i in range(1, 11):
        print(f"  fig{i:02d}_*.png")


if __name__ == "__main__":
    main()
