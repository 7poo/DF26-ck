def analyze_large_codes(rows_tr, rows_va, rows_te, y_tr, y_va):
    """
    Large codes (>10 000) là mã sản phẩm / nhà máy.
    Chứng minh chúng predict mạnh attr_3 và attr_6.
    """
    # Collect frequency of each large code
    large_freq_tr = Counter(v for r in rows_tr for v in r if v > LARGE_THRESH)
    large_freq_te = Counter(v for r in rows_te for v in r if v > LARGE_THRESH)

    # For each large code: mean/std of attr_3 and attr_6
    code_stats_tr = defaultdict(list)
    for i, r in enumerate(rows_tr):
        for v in r:
            if v > LARGE_THRESH:
                code_stats_tr[v].append((int(y_tr[i, 2]), int(y_tr[i, 5])))

    code_summary_tr = {}
    for code, pairs in code_stats_tr.items():
        if len(pairs) < 20:
            continue
        a3 = [p[0] for p in pairs]
        a6 = [p[1] for p in pairs]
        code_summary_tr[code] = {
            "n": len(pairs),
            "attr3_mean": float(np.mean(a3)),
            "attr3_std":  float(np.std(a3)),
            "attr6_mean": float(np.mean(a6)),
            "attr6_std":  float(np.std(a6)),
        }

    # Same for val — check consistency
    code_stats_va = defaultdict(list)
    for i, r in enumerate(rows_va):
        for v in r:
            if v > LARGE_THRESH:
                code_stats_va[v].append((int(y_va[i, 2]), int(y_va[i, 5])))

    # Cross-split consistency: how stable is mean attr_3 per large code?
    common_codes = set(code_summary_tr.keys()) & {c for c, p in code_stats_va.items() if len(p) >= 10}
    consistency = {}
    for code in sorted(common_codes):
        va_a3 = [p[0] for p in code_stats_va[code]]
        tr_mean = code_summary_tr[code]["attr3_mean"]
        va_mean = float(np.mean(va_a3))
        consistency[code] = {
            "tr_mean_a3": tr_mean,
            "va_mean_a3": va_mean,
            "abs_diff": abs(tr_mean - va_mean),
        }

    # first_large_code in sequence: does it predict attr_3?
    first_large_lkp = defaultdict(list)
    for i, r in enumerate(rows_tr):
        for v in r:
            if v > LARGE_THRESH:
                first_large_lkp[v].append(int(y_tr[i, 2]))
                break

    # Presence of ANY large code: correlation with attr_3
    has_large_tr = np.array([any(v > LARGE_THRESH for v in r) for r in rows_tr])
    corr_large_a3 = np.corrcoef(has_large_tr.astype(float), y_tr[:, 2])[0, 1]
    corr_large_a6 = np.corrcoef(has_large_tr.astype(float), y_tr[:, 5])[0, 1]

    n_large_tr = np.array([sum(1 for v in r if v > LARGE_THRESH) for r in rows_tr])
    corr_nlarge_a3 = np.corrcoef(n_large_tr.astype(float), y_tr[:, 2])[0, 1]

    return {
        "top20_by_freq_train": large_freq_tr.most_common(20),
        "top20_by_freq_test":  large_freq_te.most_common(20),
        "n_unique_large_train": len(large_freq_tr),
        "n_unique_large_test":  len(large_freq_te),
        "code_summary_train": code_summary_tr,
        "cross_split_consistency": consistency,
        "corr_has_large_vs_attr3": float(corr_large_a3),
        "corr_has_large_vs_attr6": float(corr_large_a6),
        "corr_n_large_vs_attr3":   float(corr_nlarge_a3),
        "top_predictive_for_a3": sorted(
            [(c, s["attr3_mean"], s["attr3_std"], s["n"]) for c, s in code_summary_tr.items()],
            key=lambda x: -x[1]
        )[:15],
    }


