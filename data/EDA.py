# SECTION 6 — TARGET ANALYSIS
# ============================================================
def analyze_targets(Y_train, Y_val):
    corr_tr = Y_train[TARGET_COLS].corr().round(4)
    corr_va = Y_val[TARGET_COLS].corr().round(4)

    tv = {c: total_variation(
        Y_train[c].value_counts(normalize=True),
        Y_val[c].value_counts(normalize=True)
    ) for c in TARGET_COLS}

    # Month-specific: train skewed (months 3, 11, 12 dominant)?
    a1_tr = Y_train["attr_1"].value_counts(normalize=True).sort_index()
    a1_va = Y_val["attr_1"].value_counts(normalize=True).sort_index()

    duration = {}
    for name, ydf in [("train", Y_train), ("val", Y_val)]:
        start = (ydf["attr_1"] - 1) * 31 + ydf["attr_2"]
        end   = (ydf["attr_4"] - 1) * 31 + ydf["attr_5"]
        dur   = end - start
        duration[name] = {
            "mean": float(dur.mean()), "median": float(dur.median()),
            "neg_ratio": float((dur < 0).mean()),
            "zero_ratio": float((dur == 0).mean()),
        }

    equality = {
        "train_month_eq": float((Y_train["attr_1"] == Y_train["attr_4"]).mean()),
        "val_month_eq":   float((Y_val["attr_1"]   == Y_val["attr_4"]).mean()),
        "train_day_eq":   float((Y_train["attr_2"] == Y_train["attr_5"]).mean()),
        "val_day_eq":     float((Y_val["attr_2"]   == Y_val["attr_5"]).mean()),
    }

    # attr_3 / attr_6 uniformity test
    attr3_tr_counts = Y_train["attr_3"].value_counts().sort_index()
    attr3_expected  = len(Y_train) / 100.0
    uniformity_attr3 = float(attr3_tr_counts.std() / attr3_expected)

    return {
        "correlation_train": corr_tr.to_dict(),
        "correlation_val":   corr_va.to_dict(),
        "total_variation":   tv,
        "attr1_skew_train_top3": a1_tr.nlargest(3).to_dict(),
        "attr1_skew_val_top3":   a1_va.nlargest(3).to_dict(),
        "duration": duration,
        "equality": equality,
        "attr3_uniformity_cv": uniformity_attr3,
    }


