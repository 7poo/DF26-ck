# SECTION 7 — DUPLICATE / LABEL QUALITY
# ============================================================
def analyze_quality(rows_tr, y_tr):
    key_labels = defaultdict(set)
    key_count  = Counter(tuple(r) for r in rows_tr)
    for r, yy in zip(rows_tr, y_tr):
        key_labels[tuple(r)].add(tuple(int(v) for v in yy))
    n_dup      = sum(1 for k, cnt in key_count.items() if cnt > 1 and len(key_labels[k]) == 1)
    n_conflict = sum(1 for v in key_labels.values() if len(v) > 1)
    return {
        "n_unique_sequences": len(key_labels),
        "n_duplicate_keys": n_dup,
        "n_conflicting_keys": n_conflict,
        "conflict_rate": n_conflict / max(1, len(key_labels)),
    }


