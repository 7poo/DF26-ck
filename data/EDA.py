def total_variation(a, b):
    idx = sorted(set(a.index) | set(b.index))
    pa = np.array([a.get(i, 0.0) for i in idx], dtype=np.float64)
    pb = np.array([b.get(i, 0.0) for i in idx], dtype=np.float64)
    return 0.5 * np.abs(pa - pb).sum()

def weighted_score(y_true, y_pred):
    err = ((y_true.astype(np.float32) - y_pred.astype(np.float32)) / M_NORM) ** 2
    return float(np.mean(np.sum(err * W_METRIC, axis=1) / 6.0))

def token_presence(rows, token):
    return float(sum(token in r for r in rows) / max(1, len(rows)))

def majority_rule(train_rows, y_vals, key_fn):
    bucket = defaultdict(list)
    for r, yy in zip(train_rows, y_vals):
        k = key_fn(r)
        if k is None: continue
        bucket[k].append(int(yy))
    out = {}
    for k, vals in bucket.items():
        cnt = Counter(vals)
        pred, n = cnt.most_common(1)[0]
        out[k] = (pred, n / len(vals), len(vals))
    return out

def eval_rule(val_rows, y_val, lookup, key_fn, min_n=3, min_purity=0.95):
    covered = correct = 0
    for r, y in zip(val_rows, y_val):
        k = key_fn(r)
        if k is None or k not in lookup: continue
        pred, pur, n = lookup[k]
        if n < min_n or pur < min_purity: continue
        covered += 1
        correct += int(pred == int(y))
    return {
        "coverage": covered / max(1, len(val_rows)),
        "accuracy_on_covered": correct / max(1, covered),
        "covered_rows": int(covered),
    }

def pospair_lookup(rows, y_vals, positions, rev=False):
    bucket = defaultdict(list)
    for r, yy in zip(rows, y_vals):
        L = len(r)
        idxs, ok = [], True
        for p in positions:
            idx = L - p if rev else p
            if idx < 0 or idx >= L: ok = False; break
            idxs.append(idx)
        if not ok: continue
        bucket[tuple(r[j] for j in idxs)].append(int(yy))
    out = {}
    for k, vals in bucket.items():
        cnt = Counter(vals)
        best, n = cnt.most_common(1)[0]
        out[k] = (best, n / len(vals), len(vals))
    return out


