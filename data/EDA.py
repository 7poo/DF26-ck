def analyze_positional_rules(rows_tr, rows_va, y_tr, y_va):
    rules = {
        # prefix → start date
        "f1→a1":   (lambda r: (r[0],) if len(r)>=1 else None, 0, 5, 0.95),
        "f2→a1":   (lambda r: tuple(r[:2]) if len(r)>=2 else None, 0, 10, 0.95),
        "f3→a1":   (lambda r: tuple(r[:3]) if len(r)>=3 else None, 0, 5, 0.95),
        "f3→a2":   (lambda r: tuple(r[:3]) if len(r)>=3 else None, 1, 10, 0.95),
        "f4→a2":   (lambda r: tuple(r[:4]) if len(r)>=4 else None, 1, 5,  1.00),
        # suffix → end date / factory
        "l2s→a6":  (lambda r: tuple(strip104(r)[-2:]) if len(strip104(r))>=2 else None, 5, 10, 0.95),
        "l3s→a6":  (lambda r: tuple(strip104(r)[-3:]) if len(strip104(r))>=3 else None, 5, 5,  1.00),
        "l2s→a45": None,   # handled separately (multi-output)
        "l3s→a4":  (lambda r: tuple(strip104(r)[-3:]) if len(strip104(r))>=3 else None, 3, 5, 1.00),
        "l3s→a5":  (lambda r: tuple(strip104(r)[-3:]) if len(strip104(r))>=3 else None, 4, 5, 1.00),
        # middle → attr_3
        "mid45→a3":  (lambda r: tuple(r[4:6]) if len(r)>=6 else None, 2, 5, 0.98),
        "rev67→a3":  (lambda r: tuple(r[-7:-5]) if len(r)>=7 else None, 2, 5, 0.98),
        "mid23→a3":  (lambda r: tuple(r[2:4]) if len(r)>=4 else None, 2, 5, 0.98),
        # full split pattern
        "full_seq→all": None,   # handled separately
    }

    results = {}
    for name, spec in rules.items():
        if spec is None:
            continue
        fn, col_idx, min_n, min_p = spec
        lkp = majority_rule(rows_tr, y_tr[:, col_idx], fn)
        res = eval_rule(rows_va, y_va[:, col_idx], lkp, fn, min_n=min_n, min_purity=min_p)
        # Also measure accuracy on val with purity >= 0.98
        lkp_high = {k: v for k, v in lkp.items() if v[1] >= 0.98}
        cov_high = sum(1 for r in rows_va if fn(r) in lkp_high)
        results[name] = {**res, "n_lookup_keys": len(lkp), "n_highpurity_keys": len(lkp_high)}

    # l2s→a45 (multi-output)
    lkp_a45 = defaultdict(list)
    for r, yy in zip(rows_tr, y_tr):
        s = strip104(r)
        if len(s) < 2: continue
        lkp_a45[tuple(s[-2:])].append((int(yy[3]), int(yy[4])))
    a45_det = {}
    for k, vals in lkp_a45.items():
        if len(vals) < 10: continue
        cnt = Counter(vals); best, n = cnt.most_common(1)[0]
        if n / len(vals) >= 0.90:
            a45_det[k] = (best, n / len(vals))
    cov_a45 = acc_a45 = 0
    for r, yy in zip(rows_va, y_va):
        s = strip104(r)
        k = tuple(s[-2:]) if len(s) >= 2 else None
        if k not in a45_det: continue
        cov_a45 += 1
        pred, _ = a45_det[k]
        acc_a45 += int(pred == (int(yy[3]), int(yy[4])))
    results["l2s→a45"] = {
        "coverage": cov_a45 / max(1, len(rows_va)),
        "accuracy_on_covered": acc_a45 / max(1, cov_a45),
        "covered_rows": cov_a45,
        "n_lookup_keys": len(a45_det),
    }

    # Full exact sequence lookup
    exact_lkp = {}
    for r, yy in zip(rows_tr, y_tr):
        k = tuple(r)
        if k not in exact_lkp:
            exact_lkp[k] = tuple(int(v) for v in yy)
    cov_exact = acc_exact = 0
    for r, yy in zip(rows_va, y_va):
        k = tuple(r)
        if k not in exact_lkp: continue
        cov_exact += 1
        acc_exact += int(exact_lkp[k] == tuple(int(v) for v in yy))
    results["full_exact"] = {
        "coverage": cov_exact / max(1, len(rows_va)),
        "accuracy_on_covered": acc_exact / max(1, cov_exact),
        "covered_rows": cov_exact,
        "n_lookup_keys": len(exact_lkp),
    }

    # Positional importance: for each position p, what is majority-rule accuracy for attr_1?
    pos_accuracy = {}
    for p in range(10):
        fn = lambda r, _p=p: (r[_p],) if len(r) > _p else None
        lkp = majority_rule(rows_tr, y_tr[:, 0], fn)
        res = eval_rule(rows_va, y_va[:, 0], lkp, fn, min_n=5, min_purity=0.90)
        pos_accuracy[f"pos{p}_f1"] = res

    return results, pos_accuracy


