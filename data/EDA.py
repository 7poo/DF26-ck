def analyze_special_tokens(rows_tr, rows_va, rows_te, y_tr, y_va):
    """
    Phân tích chi tiết từng nhóm token đặc biệt:
      - Position analysis (vị trí xuất hiện trong chuỗi)
      - Co-occurrence với token khác
      - Predictive power đối với từng target
      - Presence shift qua train/val/test
    """
    results = {}

    for split_name, rows in [("train", rows_tr), ("val", rows_va), ("test", rows_te)]:
        presence = {}
        for tok in ALL_SPECIAL:
            n_has = sum(1 for r in rows if tok in r)
            n_first = sum(1 for r in rows if len(r) > 0 and r[0] == tok)
            n_last  = sum(1 for r in rows if len(r) > 0 and r[-1] == tok)
            # Mean position when present (normalized by seq length)
            positions = []
            for r in rows:
                for i, v in enumerate(r):
                    if v == tok:
                        positions.append(i / max(1, len(r) - 1))
            presence[tok] = {
                "pct_has": n_has / max(1, len(rows)),
                "pct_first": n_first / max(1, len(rows)),
                "pct_last": n_last / max(1, len(rows)),
                "mean_rel_position": float(np.mean(positions)) if positions else None,
                "n_occurrences": len(positions),
            }
        results[split_name] = presence

    # Predictive power of structural tokens on train
    tok_pred = {}
    for tok in list(STRUCT_TOKENS) + list(ROUTE_TOKENS):
        has_tok = [i for i, r in enumerate(rows_tr) if tok in r]
        no_tok  = [i for i, r in enumerate(rows_tr) if tok not in r]
        if not has_tok or not no_tok:
            continue
        stats = {}
        for j, col in enumerate(TARGET_COLS):
            y_has = y_tr[has_tok, j]
            y_no  = y_tr[no_tok, j]
            stats[col] = {
                "mean_has": float(np.mean(y_has)),
                "mean_no":  float(np.mean(y_no)),
                "diff":     float(np.mean(y_has) - np.mean(y_no)),
            }
        tok_pred[tok] = stats

    # 103 position analysis: where is last 103 in sequence?
    pos103_rel = []
    for r in rows_tr:
        p103 = [i for i, v in enumerate(r) if v == 103]
        if p103:
            pos103_rel.append(p103[-1] / max(1, len(r) - 1))
    results["pos103_relative_distribution"] = {
        "mean": float(np.mean(pos103_rel)) if pos103_rel else None,
        "median": float(np.median(pos103_rel)) if pos103_rel else None,
        "pct_in_first_half": float(np.mean([p < 0.5 for p in pos103_rel])) if pos103_rel else None,
    }

    # 104 position in boss sequences
    boss_tr = [r for r in rows_tr if 103 not in r and 104 in r]
    pos104_rel = [r.index(104) / max(1, len(r) - 1) for r in boss_tr if 104 in r]
    results["boss_104_position"] = {
        "n_boss": len(boss_tr),
        "pct_104_at_last": float(np.mean([r[-1] == 104 for r in boss_tr])),
        "mean_relative_pos": float(np.mean(pos104_rel)) if pos104_rel else None,
    }

    # 609 routing: always first?
    route609_tr = [r for r in rows_tr if 609 in r]
    results["token_609_routing"] = {
        "n_has_609": len(route609_tr),
        "pct_609_first": float(np.mean([r[0] == 609 for r in route609_tr])) if route609_tr else 0.0,
        "pct_no_103_when_609": float(np.mean([103 not in r for r in route609_tr])) if route609_tr else 0.0,
        "pct_no_104_when_609": float(np.mean([104 not in r for r in route609_tr])) if route609_tr else 0.0,
    }

    # perfect-predictor feature_1 codes
    perfect_f1_stats = {}
    for code, month in PERFECT_F1.items():
        rows_with = [i for i, r in enumerate(rows_tr) if len(r) > 0 and r[0] == code]
        if rows_with:
            a1_vals = y_tr[rows_with, 0]
            perfect_f1_stats[code] = {
                "predicted_month": month,
                "n_train": len(rows_with),
                "accuracy": float(np.mean(a1_vals == month)),
                "unique_a1_values": sorted(set(int(v) for v in a1_vals)),
            }
    results["perfect_f1_predictors"] = perfect_f1_stats

    return results


