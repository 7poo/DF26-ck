def analyze_regimes(rows_tr, rows_va, rows_te, y_tr, y_va, lens_tr, lens_va, lens_te):
    reg_tr = np.array([get_regime(r) for r in rows_tr])
    reg_va = np.array([get_regime(r) for r in rows_va])
    reg_te = np.array([get_regime(r) for r in rows_te])

    regime_shift = {
        "train": {int(j): float((reg_tr == j).mean()) for j in range(3)},
        "val":   {int(j): float((reg_va == j).mean()) for j in range(3)},
        "test":  {int(j): float((reg_te == j).mean()) for j in range(3)},
    }

    # Target distribution per regime (train)
    regime_targets = {}
    for rg in range(3):
        idx = np.where(reg_tr == rg)[0]
        if len(idx) == 0:
            continue
        regime_targets[rg] = {
            col: {
                "mean": float(np.mean(y_tr[idx, j])),
                "std":  float(np.std(y_tr[idx, j])),
                "median": float(np.median(y_tr[idx, j])),
            }
            for j, col in enumerate(TARGET_COLS)
        }

    # Seq length per regime
    regime_lens = {
        rg: {
            "train_mean": float(np.mean(lens_tr[reg_tr == rg])) if (reg_tr == rg).any() else 0,
            "val_mean":   float(np.mean(lens_va[reg_va == rg])) if (reg_va == rg).any() else 0,
            "test_mean":  float(np.mean(lens_te[reg_te == rg])) if (reg_te == rg).any() else 0,
        }
        for rg in range(3)
    }

    # Boss deep dive: structure of regime-2 sequences
    boss_tr = [rows_tr[i] for i in np.where(reg_tr == 2)[0]]
    boss_va = [rows_va[i] for i in np.where(reg_va == 2)[0]]
    boss_te = [rows_te[i] for i in np.where(reg_te == 2)[0]]

    # Where exactly is token 104 in boss sequences?
    pos104_in_boss = []
    for r in boss_tr:
        if 104 in r:
            pos104_in_boss.append(r.index(104))
    last_pos = [len(r) - 1 for r in boss_tr if len(r) > 0]

    # What tokens immediately precede 104?
    pre104_tokens = Counter()
    for r in boss_tr:
        for i, v in enumerate(r):
            if v == 104 and i > 0:
                pre104_tokens[r[i-1]] += 1

    # attr_6 distribution for boss
    boss_tr_idx = np.where(reg_tr == 2)[0]
    boss_va_idx = np.where(reg_va == 2)[0]
    a6_boss_tr = y_tr[boss_tr_idx, 5] if len(boss_tr_idx) else np.array([])
    a6_boss_va = y_va[boss_va_idx, 5] if len(boss_va_idx) else np.array([])

    # Boss suffix rule evaluation
    boss_a6_tr = y_tr[boss_tr_idx, 5]
    lkp_boss_l2 = majority_rule(boss_tr, boss_a6_tr, lambda r: tuple(r[-2:]) if len(r) >= 2 else None)
    lkp_boss_l3 = majority_rule(boss_tr, boss_a6_tr, lambda r: tuple(r[-3:]) if len(r) >= 3 else None)
    boss_a6_va = y_va[boss_va_idx, 5]
    boss_rule = {
        "last2_to_a6": eval_rule(boss_va, boss_a6_va, lkp_boss_l2, lambda r: tuple(r[-2:]) if len(r)>=2 else None, min_n=3, min_purity=1.0),
        "last3_to_a6": eval_rule(boss_va, boss_a6_va, lkp_boss_l3, lambda r: tuple(r[-3:]) if len(r)>=3 else None, min_n=3, min_purity=1.0),
    }

    return {
        "regime_shift": regime_shift,
        "regime_targets_train": regime_targets,
        "regime_lens": regime_lens,
        "boss_n_train": len(boss_tr),
        "boss_n_val":   len(boss_va),
        "boss_n_test":  len(boss_te),
        "boss_104_always_last": float(np.mean([r[-1] == 104 for r in boss_tr if r])),
        "boss_pre104_top5": pre104_tokens.most_common(5),
        "boss_a6_train_mean": float(np.mean(a6_boss_tr)) if len(a6_boss_tr) else 0,
        "boss_a6_val_mean":   float(np.mean(a6_boss_va)) if len(a6_boss_va) else 0,
        "boss_rule": boss_rule,
    }


